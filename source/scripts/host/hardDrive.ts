module TSOS {

    export class HardDrive {

        constructor() {

            for(var t = 0; t < 4; t++) {

                for(var s = 0; s < 8; s++) {

                    for(var b = 0; b < 8; b++) {

                        if (t === 0)
                            sessionStorage.setItem("" + t + s + b, '0&&&************************************************************************************************************************');
                        else
                            sessionStorage.setItem("" + t + s + b, '0&&&~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~');

                    }

                }

            }

            sessionStorage.setItem("000", '1&&&************************************************************************************************************************');

        }


    }


}